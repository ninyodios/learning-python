x = 100

# just a common if
if x == 100:
    print("equals")

# simple if / else
if x == 100:
    print("equals")
else:
    print("OMG, they are not the same!!!")

# if/or/else
if x == 100  or x == 200:
    print("x is equal to %", x)
else:
    print("x is different from 100 and 200")

# if/elif
if x == 100:
    print("gotcha! x is equal to %", x)
elif x > 100:
    print("your shot get high")
elif x < 100:
    print("short shot ;)")

